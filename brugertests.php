            <h2>Hvad er usability?</h2>
                
            <p>På dansk betyder det brugbarhed, når vi snakker om usability og brugbarhed er det meget enkelt at gøre en hjemmeside, apps og andre digitale produkter brugbar.</p>
                
            <h2>Forklar pilottest</h2>
                
            <p>Pilottest er at afprøve en ny testmetode inden man vælger og bruge den til det projekt man har gang i. Prøve inden afprøvning</p>
                
            <h2>Hvad er et undersøgelsesdesign?</h2>
                
            <p>Et undersøgelsesdesign er den måde man bygger en undersøgelse op med hvilke ting man skal igennem, det går fra når du begynder og forberede en undersøgelse til du har fuldført den.</p>
                
            <h2>Hvad er en gangstertest?</h2>    
            
            <p>Gangstertesten er en test som skal vise designeren af hjemmesiden om den er let et begå sig på (at finde rundt på, med informationer osv.) testpersonen får mere dybdegående spørgsmål hvor de skal give point for hver spørgsmål om hvor let det var at finde tingene på hjemmesiden, point går fra 0-3, 0 = umuligt at finde 3=let og finde max point til hjemmesiden er 18 point.</p>
                
            <h2>Hvordan hænger usability-test sammen med ressourceforbrug? </h2>
                
            <p>Måden hvorpå usability-test og ressourceforbrug hænger sammen på den måde at hvis man udfører en usability-test på ens hjemmeside skal man bruge ressourcer på "eksperter" som tester den, forskning viser at 5 eksperter finder ca 75% af fejlene på en hjemmeside i første forsøg, men at få flere end 5 eksperter giver ikke mere værdi da de vil finde de samme fejl som de 5 som allerede har testet hjemmesiden.</p>
                
            <p>Man kan sammenligne det med rengøringen derhjemme hvis du bliver ved med at hælde mere rengøringsmiddel i spanden får du ikke et renere resultat men kun et større sæbelag du skal vaske af.
            Flere kokke fordærver maden
            </p>    
                
            <h2>Forklar hvorfor usablity altid indgår i en kontekst</h2>
                
            <p>Usability indgår altid i en kontekst f.eks. i forhold til en hjemmeside for at gør den nemmere for brugeren at betjene.</p>
                
            <h2>Hvad betyder funktionalitet?</h2>
                
            <p>Funktionalitet er at hjemmesiden fungerer til de behov den skal dække samt at den er let at betjene for brugeren.</p>
                
            <h2>Hvad er brugertilfredshed?</h2>
                
            <p>Brugertilfredshed er når brugerne af hjemmesiden oplever at de er tilfredse med den oplevelse som de har af hjemmesiden og dens opbygning. f.eks. at brugeren er tilfreds med proceduren efter et køb på en webshop.</p>  
                      
            <h2>Hvad er usability-kriterier til websites?</h2>
                
            <p>Usability-kriterier er de retningslinjer der stilles op for at gøre en hjemmeside brugervenlig, f.eks. at den er let at navigere rundt på både med hvor man er og hvor man skal hen på siden for at finde det man leder efter.</p>  
                      
            <h2>Hvilke testmetoder skal man anvende?</h2>
                
            <p>Der er ikke nogle bestemte testmetoder som skal anvendes men det som er afgørende er om man vil have simple svar eller dybdegående svar f.eks. gangster testen til simple svar og ekspertvurdering til dybdegående svar.</p>  
                      
            <h2>Hvad er skitser og prototyper?</h2>
                
            <p>Skitser er billeder af hvordan man vil have en hjemmeside til at se ud ude for mange detaljer man kan sige det er tandstik-mænd for en hjemmesides design. prototyper er til gengæld en udgave af den færdige hjemmeside hvor du kan afprøve din skitse, prototypen kan være papir baseret hvor der er lavet en masse papir lapper som viser hjemmesiden.</p>  
                      
            <h2>Hvorfor og hvordan dokumenteres resultater af tests?</h2>
                
            <p>Hvorfor man dokumentere resultaterne af test er for at man kan bruge brugerens meninger til at udvikle på hjemmesiden så den passer til dem bedst muligt. Hvordan man dokumenterer resultaterne, er forskellig enten ved at tingene bliver skrevet ned eller også filmer man brugerne med den måde de håndtere prototyperne.</p>  
                      
            <h2>Hvad er en ekspertvurdering?</h2>
                
            <p>Ekspertvurdering er en testmetode hvor der er 3-5 eksperter som er nogle som kan komme til at bruge hjemmesiden, afprøver den og kommer med fejl og mangler hos den i forhold til brugervenligheden.</p>
                
