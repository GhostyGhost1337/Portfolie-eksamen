            <h2>Som opgave skulle vi skrive om emnet og researche</h2>
                
            <p>Først undersøgte jeg persondataloven</p>
                
            <p>Persondataloven omhandler følsomme data om individer som ikke må offentliggøres i nogen sammenhæng med mindre der er givet samtykke. Der er også håndtering af almindelige persondata som ikke kræver samtykke det kan være i forbindelse med jobs. 
            </p>
                
            <p>
            De almindelige data kunne være navn, adresse, tlf.nr, fødselsdato, familieforhold, cv, løn, arbejdstider, fravær og sygedage (men ikke årsager), kontonummer og skat plus gæld. Det ville være data som kunne håndteres i ansættelser. 
            </p>
            
            <p>
            Data som helbredsforhold, fagforening, Race eller etnisk baggrund, Politisk, religiøs eller filosofisk overbevisning, seksuelle forhold ville kræve samtykke at bruge. Det vil sige at en side som Facebook ikke kunne kræve at du oplyser dit helbred eller politisk syn for at oprette en profil da det ville være imod persondataloven. Her ville CPR numre også komme ind da de heller ikke må behandles medmindre brugeren giver samtykke til det. 
            </p>

            <p>
            GDPR er regler for, hvordan virksomheder skal håndtere persondata ifølge deres egen hjemmeside, er der 5 vigtige regler. En virksomhed skal føre en fortegnelse som er en kortlægning af virksomhedens processer når der behandles personoplysninger.
            </p>
                
            <p>
            En virksomhed skal også dokumentere lovgivningens principper for god behandling bliver opfyldt.
            </p>
                
            <p>
            Virksomheder skal også dokumentere at de har indført passende tekniske og organisatoriske foranstaltninger.
            </p>
                
            <p>    
            Der skal oplyses til kunder og ansatte om, hvordan deres data bliver behandlet og en virksomhed skal bevise at de følger lovgivningen når det handler om samtykke. 
            </p>
                
            <h2>Næst undersøgte jeg immaterielle rettigheder</h2>
                
            <p>Immaterielle rettigheder omhandler regler og love når det kommer til eksempelvis kunst, billeder, litteratur, opfindelser, design og forretningskendetegn.</p>
                
            <p>Designbeskyttelse kræver at ens design er nyt og ikke findes andre steder for at man kan få beskyttelse af loven.</p>
                
            <p>Domæner er også en Immateriel rettighed, hvor det handler om at den der har størst interesse i et domæne skal have det. For at overtage domæner skal man altså spørge ejeren om gratis overdragelse, betale eller klage, hvis man mener man selv har større interesse.</p>
                
            <p>Immaterielle rettigheder er beskyttet af ophavsretten som siger man ikke må kopiere eller stjæle uden tilladelse fra dem der har ophavsret.</p>
                
            <p>Har man opfundet en ny teknologi kan man søge om patent og få beskyttet sin opfindelse gennem det.</p>
                
            <h2>Den tredje ting jeg undersøgte var ophavsret</h2>
                
            <p>Ophavsret eller copyright handler om beskyttelse af værker så de ikke bliver kopieret eller brugt ulovligt.</p>
                
            <p>Ophavsretten beskytter immaterielle produkter af en lang række det kunne også være softwareprogrammer.</p>
                
            <p>Ophavsretten giver en person eller firma eneret til et produkt og giver dem lov til at fremvise og gøre det tilgængeligt for offentligheden. Det er dog tilladt at citere værket eller kopiere værker til privat forbrug men det må ikke ændres på eller bruges i en sammenhæng, hvor det krænker ophavsretsholderen.</p>
                
            <p>Som multimediedesigner kunne man søge om copyright på et grafisk produkt vi laver eller en hjemmeside.</p>
                
            <p>Når det kommer til sociale medier, må man ikke bruge andres uploads af eksempelvis billeder på deres egen hjemmeside dog skal en bruger finde sig i at ens billede kan blive delt på det sociale medie via dets funktionaliteter. Vil man linke til en youtube video på sin hjemmeside kan man også det, men brugeren der uploader skal ikke finde sig i at sit materiale bliver brugt på en krænkende måde såsom i reklamer eller politik sker dette udgør det nemlig en krænkelse af ophavsrettigheder. Vil man bruge materialer fra sociale medier til sådanne ting skal man søge samtykke fra egeren.</p>
                
            <h2>Den næste ting jeg undersøgte var varemærke</h2>
                
            <p>Et varemærke er noget der gør at en bruger kan genkende en virksomhed eksempelvis facebooks logo. En multimediedesigner kan eksempelvis ikke bruge Facbooks logo til promovere en ny hjemmeside jeg har lavet, så skulle man søge tilladelse hos Facebook om at bruge det. </p>
                
            <p>Kan ophavsretten beskytte en ide?: Ja kort sagt så kan den men man skal i et eksempel som at pitche en ide til et tv program skal man sørge for at man sender en detaljeret og dateret beskrivelse af sin ide på sit tv program til producenten. Så ens Ide skal være gennemført og det skal bevises at eksempelvis Tv2 har stjålet din Ide ved at et dokument kan åbnes på en senere dato som bevis for at du havde skrevet ideen ned og sendt den. </p>
                
            <p>Skal man registrere ophavsret af noget? Nej det sker når værket skabes men vil man have sit værk beskyttet i eksempelvis USA så er det en god ide at registrere ophavsret på sin Ide eller produkt.</p>
                
            <p>Er det tilladt at udnytte sig af domæne warehousing? Nej det er ikke lovligt at registrere et domænenavn med formålet at sælge det igen for at få profit.</p>
                
            <p>Open source betyder noget ar frit tilgængeligt eksempelvis med software kan det være kildekoden der er tilgængelig og at man derfor har lov til at redigere i det og tilpasse det til egne behov men ikke til deling eller kommercielt forbrug.</p>
                
            <h2>Det næste jeg undersøgte var licensaftaler</h2>
                
            <p>Licensaftaler er en aftale mellem 2 parter en licensgiver og en licenstager det vil mene at en licens tager ville betale for rettigheden til at bruge licensgiverens produkt eksempelvis som Dania akademiet betaler for LinkedIn sådan de studerende kan logge ind og bruge siden.</p>
                
