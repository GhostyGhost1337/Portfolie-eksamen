            <section id="paradigmer">
            <h2>Kommunikationsparadigmer</h2>
                
            <p>En anden vigtig ting vi lærte om var kommunikations paradigmer som er en måde at forstå verdenen på. Der er det samfundsvidenskabelige paradigme og det humanistiske paradigme. Det er her vi får en masse kommunikations modeller som vi burde bruge, hver gang vi skal kommunikere alt efter situationen. Dog føler jeg lidt det er blevet glemt i opgaverne vi har arbejdet med.</p>
                
            <p>Det samfundsvidenskabelige paradigme er, hvor man har fokus på afsender, afsenderen bestemmer budskabets indhold også, hvordan det skal opfattes hos modtager og har en kvantitativ tilgang som jeg kommer nærmere på senere på siden.</p>
            
            <p>En formel som ofte bliver nævnt i det samfundsvidenskabelige paradigme er Laswalds formel.</p>
                
            <img src="images/laswaldsformel.PNG">
                
            <p>Den fokusere altså, hvem afsenderen er, hvad de siger (budskabet), i hvilken kanal mediet brugt, til hvem, med hvilken effekt. Her kan man sætte sig ned med en kommunikaations situation og bruge modellen til at analysere eller selv en måde at kommunikere til sin egen modtager ved at overveje de 5 hv spørgsmål.</p>
                
            <p>Det humanistiske paradigme er, hvor man har fokus på modtager, modtageren bestemmer også budskabet her og der er som reelt en dialog og har en kvalitativ tilgang som jeg også vil komme nærmere på senere.</p>
                
            <p>En kommunikationsmodel som er relevant her er IMK-Modellen.</p>
            
            <img src="images/imkmodel.PNG">
                
            <p>IMK-modellen går ud fra at der ikke er nogen kommunikation føre både afsender og modtager. Man har en afsender og en modtager som kigger på en tekst og der er en kode som bliver skabt via kulturen teksten kommer fra så væld som mediet og genren. En Kode kunne være et sprog som skal kunne forstås før der er en kommunikation det kunne også være at teksten gør brug af slang som skal kunne forstås af modtager og afsender før der kommer noget ud af kommunikationen. </p>
            
            <p>Kilder til kommunikations paradigmer er kapitel 5 i bogen kommunikation i multimediedesign og Kommunikationsteori MMD 1.semester <a href="https://eadania.mrooms.net/course/view.php?id=1604#section-4">Powerpoint fra moodleroom</a></p>
