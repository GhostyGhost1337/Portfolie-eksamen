            <h2>Præsentationsteknik</h2>
                
            <p>Når det kommer til at presentere sig selv eller det man skal snakke om, har jeg hovedsageligt lært at man skal holde det simpelt, det kan dog være svært at holde tekst man skriver simpelt. Vi fik vist en video som er komisk omkring, hvad man ikke skal gøre i <a href="https://www.youtube.com/watch?time_continue=410&v=KbSPPFYxx3o&feature=emb_logo">powerpoint</a> og vi fik arbejdet med joharis vindue som har nogle felter som kan bruges til at andre lære noget om en selv eller at man selv lære noget om en selv. Det kan være man rør sig selv i håret når man taler foran andre og det kan andre fortælle dig.</p>
                
            <img src="images/Joharis.PNG">
                
            <p>Det er to psykologer der har lavet joharis vindue Joseph Luft og Harry Ingram. Joharis vindue har 4 felter det åbne felt, det blinde felt, det skjulte felt og det ukendte felt. Som set på billedet er det så delt op i, hvad du ved og andre ved om dig selv. På billedet kan man så også se, hvordan det åbne felt kan udvides eller gøres større. Joharis vindue kan så bruges til at danne nogle rammer for kommunikationen mellem to parter. Det kunne være man ville dele noget fra det skjulte felt til et kundemøde eller spørge nogen om observationer når du holder tale til at udvide det ukendte felt.</p>
            
