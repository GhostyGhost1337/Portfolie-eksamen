            <section id="teori">
            <h2>Teori</h2>
                
            <p>Gestaltlovene til webdesign.</p>
                
            <img src="images/gestalt1.PNG">
                
            <p>På dansk er nogle af dem også kendt som nærhed (proximity) kontinuitet (continuation) lighed (similarity) lukkethed (closure) figur/baggrund (figure ground)</p>
                
            <p>Tager man nærhed er ideen at ting der er tætte opfattes som at være relaterede mere end ting der er langt fra hinanden. Det vil sige i webdesign så ville du eksempelvis placere links i din navigation tæt sammen da de har den relation at de alle tilhøre navigationen af sitet.</p>
                
            <p>Tager man kontinuitet så ville det eksempelvis være en brødkrumme på en side den køre ud i et og viser dig, hvor du har været de ting har relationen til hinanden og forstås som din vej til der, hvor du er på hjemmesiden.</p>
                
            <p>Lighed altså objekter der ligner hinanden bliver også set som at være en del af en gruppe. Det kunne være farver til knapper med links der omhandler det samme, så ville man kunne se at de hænger sammen med hinanden.</p>
            
            <p>Lukkethed ville være et eksempel med en sidebar, hvor teksten er lukket inde i en boks så ville man vide at det alt sammen har relevans til hinanden.</p>
                
            <p>Figur/baggrund handler om at den mindste figur på et areal vil være figuren, hvor den større figur ville være baggrunden.</p>
                
            <p>Common fate handler om at objekter der bevæger sig i samme retning, har relation til hinanden i forhold til objekter der ikke bevæger sig eller i en anden retning.</p>
                
            <p>Symmetry er loven der handler om at man skal have orden på sin webside de ting der høre sammen skal ikke være svære at finde.</p>
                
            <p>Kilde PDF fra <a href="https://eadania.mrooms.net/course/view.php?id=1604#section-17">moodleroom gestaltlovene</a> og de 5 andyrudlegde links </p>
