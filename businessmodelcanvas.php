            <h2>Teori</h2>
                
            <p>Business model canvas eller BMC er en model der bliver brugt til at forstå en business som kan bruges til at analysere sin forretning eller planlægge ved at sætte nogle hovedpunkter op i modellen som har punkterne Nøglepartnere NP, nøgleaktiviteter NA, nøgleressourcer NR, omkostninger OS, værditilbud VT, kunderelationer KR, kanaler K, kundesegmenter KS og indtægt IS.</p>
                
            <p>I gruppe arbejde, havde jeg så sat nogle punkter på en BMC over Facebook</p>
                
            <img src="images/bmc.PNG">
                
            <p>Men generelt ville KS være målgruppen. VT er er samlingen af produkter og ydelser der skaber værdi for et firma. K er, hvordan en virksomhed kommunikerer eksempelvis gennem egen hjemmeside eller facebook side. KR er relationen en virksomhed har med, hver af deres kundesegmenter KS. IS er så simpelt er der, hvor en virksomheds indtægter kommer fra. NR beskriver de vigtigste aktiver som er nødvendige for at skabe sin værdi. NA er det vigtigste aktiviteter en virksomhed skal gøre for at få sin forretning til at fungere. NP er de vigtige partnere som gør at forretningen kan stå på benene. OS er så de omkostninger en virksomhed har eksempelvis lønudbetaling eller indkøb af materialer.</p>
                
