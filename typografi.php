            <section id="typografi">
            <p>Typografi er et vigtigt tool for multimedie designere fordi man altid skal tænke på om målgruppen kan læse den font-type man har skrevet i. Hvad er en font-type så? Det kunne være Calibri eller Arial, Times new roman osv altså skrifttyper.</p>
                
            <img src="images/typo1.PNG">
                
            <p>Som multimedie designer ville man ofte arbejde med sans-serif skrifttyper, da de er nemme at læse, hvad er sans-serif så? Sans-serif skrifttyper her uden fødder i modsætningen til billedet over så har alle bogstaverne (der kan det) fødder de små spidser der stikker ud af bunden af bogstaverne. Det ville man gøre fordi det som reelt er nemmere at læse en skrifttype uden fødder så det er generelt mere læsevenligt.</p>
                
            <p>Når man snakker om skrifttyper så har man nogle termer til at beskrive deres udseende.</p>
                
            <img src="images/typo2.PNG">
                
            <p>Hver skrifttype kan altså have forskellige værdier på alle de her termer. Det kunne være en skrifttype havde en stor afstand mellem ord altså tracking ville være sætte stor afstand mellem dem.</p>
                
            <p>Det kunne også være kerning ville lave en lille eller stor afstand mellem bogstaver.</p>
                
            <p>Alt efter sin målgruppe og situationen kan man vælge forskellige skrifttyper, det kan være man vil invitere til en fin galla, hvor der nok ikke ville være noget problem i at få en invitation med fødder fordi det ser mere fint ud end uden fødderne.</p>
                
